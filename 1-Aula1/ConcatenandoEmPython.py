# concatenando Strings 
# nao precisa de tipagem (colocar string, duble etc.)
PrimeiroNome = "leonardo"
UltimoNome = "Vitalino"
NomeCompleto = PrimeiroNome+" "+UltimoNome
print("nome completo:",NomeCompleto)

# transformando para letras maiusculas
# para mudar para letras maiusculas eu coloco o mesmo nome da variavel, apenas acrescento o ".upper()"
print("Nome completo em letra maiuscula:", NomeCompleto.upper())